// scripts/sort-pools.ts
import fs from "fs";
import path from "path";
import { pathToFileURL } from "url";

const INPUT_FILE = path.resolve("src/mainnet/POOLS.ts");
const OUTPUT_FILE = INPUT_FILE; // overwrite same file

// dynamically import the existing POOLS
const { POOLS } = await import(pathToFileURL(INPUT_FILE).href);

// sort entries by value
const sortedEntries = Object.entries(POOLS).sort(
  ([_k1, v1], [_k2, v2]) => (v1 as number) - (v2 as number)
);

// generate TS code
const lines: string[] = [];
lines.push("// AUTO-GENERATED BY scripts/sort-pools.ts");
lines.push("// Do not edit by hand.\n");
lines.push("export const POOLS: Record<string, number> = {");
for (const [key, value] of sortedEntries) {
  lines.push(`  ${key}: ${value},`);
}
lines.push("};");
lines.push(""); // trailing newline

const outputCode = lines.join("\n");

fs.writeFileSync(OUTPUT_FILE, outputCode, "utf8");

console.log(`Sorted POOLS written to ${OUTPUT_FILE}`);
